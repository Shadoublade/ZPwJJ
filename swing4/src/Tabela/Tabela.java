    private void generator(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generator
        // TODO add your handling code here:
        Random r = new Random();
        int m = jSlider1.getValue(), n = jSlider2.getValue();
        double x = 0;
        for (int i = 0; i < jTable1.getRowCount();i++){
            for (int j = 0; j < jTable1.getColumnCount(); j++){
                if (jRadioButton1.isSelected()) x = m + r.nextInt(n - m + 1);
                if (jRadioButton2.isSelected()) x = m + (n - m) * r.nextDouble();
                jTable1.setValueAt(x, i, j);
            }
        }
    }//GEN-LAST:event_generator

