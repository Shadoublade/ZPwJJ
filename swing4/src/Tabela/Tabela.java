    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        try {
            FileWriter wy = new FileWriter("wyniki.txt");
            wy.write("wylosowane liczby z przedziału <" + jSlider1.getValue()+ "," + jSlider2.getValue() + ">\n");
            for (int i = 0; i < jTable1.getRowCount(); i++){
                for (int j = 0; j < jTable1.getColumnCount(); j++)
                    wy.write("" + (jTable1.getValueAt(i,j)));
                wy.write ("\n");
            }
            wy.close();
            JOptionPane.showMessageDialog(this, "Wyniki zostały zapisane w pliku \"wyniki.txt\"");
        } catch (IOException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

